import 'package:flutter/cupertino.dart';
import 'package:flutter/material.dart';
import 'package:google_maps_flutter/google_maps_flutter.dart';

class MapPage extends StatefulWidget {
  const MapPage({Key? key}) : super(key: key);

  @override
  State<MapPage> createState() => _MapPageState();
}

class _MapPageState extends State<MapPage> {
  final Set<Marker> _markers = {};
  late GoogleMapController _googleMapController;

  late BitmapDescriptor mapMarker;
  void setCustomMarker() async {
    mapMarker = await BitmapDescriptor.fromAssetImage(const ImageConfiguration(), 'assets/images/marker.png');
  }

  @override
  void initState() {
    super.initState();
    setCustomMarker();
  }

  @override
  void dispose() {
    _googleMapController.dispose();
    super.dispose();
  }

  static const _initialCameraPos = CameraPosition(
    target: LatLng(37.773972, -122.431297),
    zoom: 15,
  );
  @override
  Widget build(BuildContext context) {
    return CupertinoPageScaffold(
      child: Stack(
        children: [
          GoogleMap(
            initialCameraPosition: _initialCameraPos,
            myLocationButtonEnabled: false,
            onMapCreated: (controller) {
              controller.setMapStyle(Utils.mapStyle);
              _googleMapController = controller;

              setState(() {
                _markers.add(
                  Marker(
                    icon: mapMarker,
                    markerId: const MarkerId('1'),
                    position: const LatLng(37.773972, -122.431297),
                    infoWindow: const InfoWindow(
                      title: 'Title',
                      snippet: 'Snippet',
                    )
                  )
                );
              });
            },
            markers: _markers,
          ),
          Positioned(
            right: 30,
            bottom: 30,
            child: FloatingActionButton(
              onPressed: () => _googleMapController.animateCamera(CameraUpdate.newCameraPosition(_initialCameraPos)),
              child: const Icon(Icons.center_focus_strong),
            ),
          ),
        ],
      )
    );
  }
}

